package sec01.exam01_primitivetype;

public class IntExample {

	public static void main(String[] args) {
		
		int var1 = 10; //10진수
		int var2 = 012; //2진수
		int var3 = 0xA; //16진수
		int var4 = 0x10;
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		System.out.println(var4);

	}

}
