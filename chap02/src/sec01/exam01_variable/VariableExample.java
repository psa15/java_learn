package sec01.exam01_variable;

public class VariableExample {

	public static void main(String[] args) {
		//변수 선언? 기억 장소를 생성하고 가리키는 이름을 정의
		//데이터 타입: 기억장소를 생성하는 기능
		
		//int형 데이터 타입으로 기억장소를 생성하고, 이름을 value로 부른다.
		//1)
		int value1 = 10; //초기값
		//2)
		int value2;
		value2 = 20;
		
		int value3 = value1 + value2;
		//System.out.println() : 콘솔 화면에 데이터를 출력하는 기능
		System.out.println(value3);
		

	}

}
