package sec01.exam01_api_specification;

public class ApiSpecificationExample {
/*
 * java.lang패키지: 기본패키지, 기본적인 클래스(인터페이스)들을 모아놓은 패키지
 * import 생략됨
 */
	public static void main(String[] args) {
		String name = "홍길동"; //java.lang.String name = "홍길동";
		System.out.println(name); //java.lang.System.out.println(name);

	}

}
